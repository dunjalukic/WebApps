class Author {
    constructor(name, id, username, email, phone, street, city, zip, company, slogan) {
        this.name = name
        this.id = id
        this.username = username
        this.email = email
        this.phone = phone
        this.street = street
        this.city = city
        this.zip = zip
        this.company = company
        this.slogan = slogan
    }
}

export default Author;